	// Shift second row one to the right
	tmp       = state[1];
	state[1]  = state[13];
	state[13] = state[9];
	state[9]  = state[5];
	state[5]  = tmp;

	// Shift third row two to the right
	tmp       = state[2];
	state[2]  = state[10];
	state[10] = tmp;
	tmp       = state[6];
	state[6]  = state[14];
	state[14] = tmp;

