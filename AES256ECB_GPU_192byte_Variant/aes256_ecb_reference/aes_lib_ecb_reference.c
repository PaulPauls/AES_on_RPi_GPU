/* XORs the state with the current roundKey
 */
void add_round_key(uint8_t *state, const uint8_t *expandedKey, int round)
{
	for (int i = 0; i < 16; i++) {
		state[i] ^= expandedKey[(16 * round) + i];
	}
}



